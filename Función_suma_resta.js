function suma(a, b) {
  return a + b;
}

function resta(a, b) {
  return a - b;
}

// Ejemplo de uso de las funciones de suma y resta
const num1 = 10;
const num2 = 5;

console.log(`La suma de ${num1} y ${num2} es: ${suma(num1, num2)}`);
console.log(`La resta de ${num1} y ${num2} es: ${resta(num1, num2)}`);